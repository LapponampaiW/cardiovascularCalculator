// ─── Helper math ───────────────────────────────────────────────────────────────

function sq(x: number): number { return x * x; }

export function calcBMI(weightKg: number, heightCm: number): number {
  return weightKg / Math.pow(heightCm / 100, 2);
}

export function calcEGFR(creatMgdl: number, age: number, sex: 0 | 1): number {
  const kappa     = sex === 0 ? 0.7  : 0.9;
  const alpha     = sex === 0 ? -0.241 : -0.302;
  const sexFactor = sex === 0 ? 1.012 : 1;
  const ratio = creatMgdl / kappa;
  return (
    142 *
    Math.pow(Math.min(ratio, 1), alpha) *
    Math.pow(Math.max(ratio, 1), -1.2) *
    Math.pow(0.9938, age) *
    sexFactor
  );
}

// ─── Core logit function ───────────────────────────────────────────────────────

interface LogitParams {
  age: number;
  totalChol: number;   // mg/dL
  hdlChol: number;     // mg/dL
  systolicBP: number;  // mmHg
  diabetes: 0 | 1;
  smoking: 0 | 1;
  bmi: number;
  egfr: number;
  onHypertensionMed: 0 | 1;
  onStatin: 0 | 1;
  hbA1c: number | null;
  uacr: number | null;
}

function calcRiskLogit(n: number, p: LogitParams): number {
  const ageTerm    = (p.age - 55) / 10;
  const nonHDL     = (p.totalChol - p.hdlChol) * 0.02586 - 3.5;
  const hdl        = (p.hdlChol * 0.02586 - 1.3) / 0.3;
  const sbpLow     = (Math.min(p.systolicBP, 110) - 110) / 20;
  const sbpHigh    = (Math.max(p.systolicBP, 110) - 130) / 20;
  const bmiLow     = (Math.min(p.bmi, 30) - 25) / 5;
  const bmiHigh    = (Math.max(p.bmi, 30) - 30) / 5;
  const egfrLow    = (Math.min(p.egfr, 60) - 60) / -15;
  const egfrHigh   = (Math.max(p.egfr, 60) - 90) / -15;

  const RL =
    coeff[0][n]  * ageTerm +
    coeff[1][n]  * sq(ageTerm) +
    coeff[2][n]  * nonHDL +
    coeff[3][n]  * hdl +
    coeff[4][n]  * sbpLow +
    coeff[5][n]  * sbpHigh +
    coeff[6][n]  * p.diabetes +
    coeff[7][n]  * p.smoking +
    coeff[8][n]  * bmiLow +
    coeff[9][n]  * bmiHigh +
    coeff[10][n] * egfrLow +
    coeff[11][n] * egfrHigh +
    coeff[12][n] * p.onHypertensionMed +
    coeff[13][n] * p.onStatin +
    coeff[14][n] * p.onHypertensionMed * sbpHigh +
    coeff[15][n] * p.onStatin * nonHDL +
    coeff[16][n] * ageTerm * nonHDL +
    coeff[17][n] * ageTerm * hdl +
    coeff[18][n] * ageTerm * sbpHigh +
    coeff[19][n] * ageTerm * p.diabetes +
    coeff[20][n] * ageTerm * p.smoking +
    coeff[21][n] * bmiHigh +
    coeff[22][n] * ageTerm * egfrLow +
    coeff[31][n];

  // HbA1c add-on factor
  const a1cFactor =
    p.hbA1c === null
      ? coeff[30][n]
      : p.diabetes
        ? (p.hbA1c - 5.3) * coeff[28][n]
        : (p.hbA1c - 5.3) * coeff[29][n];

  // UACR add-on factor
  const uacrFactor =
    p.uacr === null
      ? coeff[27][n]
      : Math.log(p.uacr) * coeff[26][n];

  // SDI factor — no zip code lookup; use fallback coeff[25]
  const sdiFactor = coeff[25][n];

  return RL + a1cFactor + uacrFactor + sdiFactor;
}

function logistic(logit: number): number {
  return 100 * Math.exp(logit) / (1 + Math.exp(logit));
}

// ─── Unit conversion helpers ───────────────────────────────────────────────────

function toNum(s: string): number { return parseFloat(s); }

function convertHeight(val: string, unit: string): number {
  const n = toNum(val);
  return unit === 'in' ? n * 2.54 : n;
}
function convertWeight(val: string, unit: string): number {
  const n = toNum(val);
  return unit === 'lb' ? n * 0.453592 : n;
}
function convertChol(val: string, unit: string): number {
  const n = toNum(val);
  return unit === 'mmol/L' ? n * 38.67 : n;
}
function convertCreatinine(val: string, unit: string): number {
  const n = toNum(val);
  return unit === 'μmol/L' ? n * 0.011312 : n;
}

