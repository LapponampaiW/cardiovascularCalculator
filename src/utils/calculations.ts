export interface PreventResults {
  bmi: number | null;
  egfr: number | null;
  ascvd10: number | null;
  hf10: number | null;
  cvd10: number | null;
  ascvd30: number | null;
  hf30: number | null;
  cvd30: number | null;
  modelType: 'base' | 'addon';
  errors: string[];
}

export function calculatePrevent(inputs: FormInputs): PreventResults {
  const errors: string[] = [];

  // Parse & convert all inputs
  const age      = toNum(inputs.age);
  const sex      = inputs.sex === 'Male' ? 1 : 0 as 0 | 1;
  const heightCm = convertHeight(inputs.height, inputs.heightUnit);
  const weightKg = convertWeight(inputs.weight, inputs.weightUnit);
  const totalChol = convertChol(inputs.totalChol, inputs.totalCholUnit);
  const hdlChol   = convertChol(inputs.hdlChol, inputs.hdlCholUnit);
  const sbp       = toNum(inputs.systolicBP);
  const creat     = convertCreatinine(inputs.creatinine, inputs.creatinineUnit);
  const onHTNMed  = inputs.onHypertensionMed === 'Yes' ? 1 : 0 as 0 | 1;
  const onStatin  = inputs.onStatin === 'Yes' ? 1 : 0 as 0 | 1;
  const diabetes  = inputs.hasDiabetes === 'Yes' ? 1 : 0 as 0 | 1;
  const smoking   = inputs.isSmoking === 'Yes' ? 1 : 0 as 0 | 1;
  const hbA1c     = inputs.hbA1c.trim() !== '' ? toNum(inputs.hbA1c) : null;
  const uacr      = inputs.uacr.trim() !== '' ? toNum(inputs.uacr) : null;

  // Check required numeric fields are valid
  const required = { Age: age, Height: heightCm, Weight: weightKg,
    'Total Cholesterol': totalChol, 'HDL Cholesterol': hdlChol,
    'Systolic BP': sbp, 'Serum Creatinine': creat };
  for (const [label, val] of Object.entries(required)) {
    if (isNaN(val)) { errors.push(`${label} is required.`); }
  }
  if (errors.length) return { bmi: null, egfr: null, ascvd10: null, hf10: null, cvd10: null, ascvd30: null, hf30: null, cvd30: null, modelType: 'base', errors };

  // Range validation
  if (age < 30 || age > 79)              errors.push('Age must be 30–79 years.');
  if (heightCm < 50 || heightCm > 300)   errors.push('Height must be 50–300 cm.');
  if (weightKg < 0 || weightKg > 362.8)  errors.push('Weight must be 0–362.8 kg.');
  if (totalChol < 130 || totalChol > 320) errors.push('Total cholesterol must be 130–320 mg/dL.');
  if (hdlChol < 20 || hdlChol > 100)     errors.push('HDL must be 20–100 mg/dL.');
  if (sbp < 90 || sbp > 200)             errors.push('Systolic BP must be 90–200 mmHg.');
  if (creat < 0.3 || creat > 30)         errors.push('Creatinine must be 0.3–30 mg/dL.');
  if (hbA1c !== null && (hbA1c < 3 || hbA1c > 15)) errors.push('HbA1c must be 3–15%.');
  if (uacr !== null && (uacr < 0 || uacr > 25000)) errors.push('UACR must be 0–25000 mg/g.');
  if (errors.length) return { bmi: null, egfr: null, ascvd10: null, hf10: null, cvd10: null, ascvd30: null, hf30: null, cvd30: null, modelType: 'base', errors };

  // Derived values
  const bmi  = calcBMI(weightKg, heightCm);
  const egfr = calcEGFR(creat, age, sex);

  // eGFR and BMI range checks (after calculation)
  if (egfr < 15 || egfr > 140) {
    errors.push('eGFR out of range (15–140 mL/min). Cannot calculate risk.');
    return { bmi, egfr, ascvd10: null, hf10: null, cvd10: null, ascvd30: null, hf30: null, cvd30: null, modelType: 'base', errors };
  }
  if (bmi < 18.5 || bmi > 39.9) {
    errors.push('BMI out of range (18.5–39.9 kg/m²). Cannot calculate risk.');
    return { bmi, egfr, ascvd10: null, hf10: null, cvd10: null, ascvd30: null, hf30: null, cvd30: null, modelType: 'base', errors };
  }

  // Determine model variant and column offsets
  const hasA1c  = hbA1c !== null;
  const hasUACR = uacr  !== null;

  let colOffset: number;
  let modelType: 'base' | 'addon';

  if (!hasA1c && !hasUACR) {
    colOffset = 48; modelType = 'base';       // base model
  } else if (hasA1c && !hasUACR) {
    colOffset = 12; modelType = 'addon';      // HbA1c only
  } else if (!hasA1c && hasUACR) {
    colOffset = 24; modelType = 'addon';      // UACR only
  } else {
    colOffset = 0;  modelType = 'addon';      // full add-on (both)
  }

  const p: LogitParams = { age, totalChol, hdlChol, systolicBP: sbp, diabetes, smoking, bmi, egfr, onHypertensionMed: onHTNMed, onStatin, hbA1c, uacr };

  // Column layout within each block: TCvd10, ASCVD10, HF10, TCvd30, ASCVD30, HF30
  // Each pair: Female = colOffset+0, Male = colOffset+1 (repeated per output at +2 intervals)
  const s = sex; // 0=F, 1=M
  const cvd10Logit   = calcRiskLogit(colOffset + 0 + s, p);
  const ascvd10Logit = calcRiskLogit(colOffset + 2 + s, p);
  const hf10Logit    = calcRiskLogit(colOffset + 4 + s, p);
  const cvd30Logit   = calcRiskLogit(colOffset + 6 + s, p);
  const ascvd30Logit = calcRiskLogit(colOffset + 8 + s, p);
  const hf30Logit    = calcRiskLogit(colOffset + 10 + s, p);

  const ascvd10 = logistic(ascvd10Logit);
  const hf10    = logistic(hf10Logit);
  const cvd10   = logistic(cvd10Logit);

  // 30-year risk not calculated for age ≥ 60
  const ascvd30 = age < 60 ? logistic(ascvd30Logit) : null;
  const hf30    = age < 60 ? logistic(hf30Logit)    : null;
  const cvd30   = age < 60 ? logistic(cvd30Logit)   : null;

  return { bmi, egfr, ascvd10, hf10, cvd10, ascvd30, hf30, cvd30, modelType, errors };
}
